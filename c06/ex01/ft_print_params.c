#include<unistd.h>

int	main(int ac, char **av)
{
	int	a ;
	int	b ;

	b = 1;
	a = 0;
	if (ac > 1)
	{
		while (b < ac)
		{
			a = 0;
			while (av[b][a] != '\0')
			{
				write(1, &av[b][a], 1);
				a++;
			}
			write(1, "\n", 1);
			b++;
		}
	}
	return (0);
}
