int	ft_sqrt(int nb)
{
	long	a;
	long	b;

	b = nb;
	if (b <= 0)
		return (0);
	if (b == 1)
		return (1);
	a = 2;
	if (b >= 2)
	{
		while (a * a <= b)
		{
			if (a * a == b)
			{
				return (a);
			}
			a++;
		}
	}
	return (0);
}
