int	ft_str_is_numeric(char *str)
{
	int	k ;

	k = 0 ;
	while (str[k] != '\0')
	{
		if ((str[k] < '0') || (str[k] > '9'))
			return (0);
			k++ ;
	}
	return (1);
}
